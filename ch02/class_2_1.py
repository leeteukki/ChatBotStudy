# 변수 사용하기
# 변수란 어떤 값을 저장해두는 것입니다.
# 값을 한번만 사용할게 아니라면 변수로 저장합니다.
# 안녕이라는 문자열을 a라는 값에 저장해서 출력해보겠습니다.
a='안녕'
print(a)

#%%
# 3을 a라는 변수에 넣어 출력해보겠습니다.
b=3
print(b)

#%%
# True라는 boolin 값을 c라는 변수에 넣어서 출력해보겠습니다.
c=True
print(c)

#%%
